// Pyklad 1
#include <iostream>
#include <conio.h>
#include <windows.h>
#include <math.h>
using namespace std;
int main()
{
    SetConsoleCP(1251);
    SetConsoleOutputCP(1251);
    // Оголошення констант та змінних:
    const double pi = 3.14159;   // оголошення константи pi
    double r;                    // радіус основи прямого кругового циліндра
    double h;                    // висота прямого кругового циліндра
    double sb;                   // площа бічної поверхні циліндра
    double sp;                   // площа повної поверхні циліндра
    double v;                    // об’єм циліндра

    // Ввід заданих величин:
    cout << "\n Введіть радіус основи циліндра: ";
    cin >> r;
    cout << "\n Введіть висоту циліндра: ";
    cin >> h;

    // Обчислення параметрів циліндра:
    sb = 2 * pi * r * h;
    sp = sb + 2 * pi * r * r;
    v = pi * r * r * h;

    // Друк результатів у вигляді таблиці:
    cout.precision(3);
    cout << "\n Вхідні величини та результати обчислень:";
    cout << "\n -----------------------------------------------------------";
    cout << "\n I Вхідні величини:  I";
    cout << "\n -----------------------------------------------------------";
    cout << "\n I Радіус основи циліндра = " << r << "  I";
    cout << "\n I Висота циліндра = " << h << "  I";
    cout << "\n -----------------------------------------------------------";
    cout << "\n I Результати обчислень:  I";
    cout << "\n -----------------------------------------------------------";
    cout << "\n I Бічна поверхня циліндра = " << sb << " I";
    cout << "\n I Повна поверхня циліндра = " << sp << " I";
    cout << "\n I Об’єм циліндра = " << v << " I";
    cout << "\n -----------------------------------------------------------\n";
    return 0;
}

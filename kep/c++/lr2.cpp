    double x, y, z, a1, a2, a3, a4, a5;
    double b1, b2, b3, b4, b5;

    // Ввід задачі
    cout << "Введіть значення трьох дійсних змінних:\n";
    cin >> x >> y >> z;

    // Обчислення виразу a
    a1 = (2 * cos(x - pi / 6)) / (0.5 + pow(sin(y), 2));
    a2 = 1 + pow(z, 2) / (3 + pow(z, 2) / 5);
    a3 = acos(-0.5);
    a4 = pow(x, y) + fabs(y + 2 * z);
    a5 = log10(2 + x * x + y * y + 1);

    // Обчислення виразу b
    b1 = exp(-3 * x * x + y / 3 - z);
    b2 = pow(tan(x), 2) + y / (3 * z + 2);
    b3 = 1 / (sin(pi / 2 - 2 * x) * sin(pi / 2 - 2 * x));
    b4 = pow(x, 3) + (y / 2);
    b5 = pow(3, x) / (y / 3);

    // Виведення результатів у вигляді таблиці
